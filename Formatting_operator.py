#Python Formatting operator
"""Python allows us to use the format specifiers used in C's printf statement. The format specifiers in python are treated in the same way as they are treated in C. However, Python provides an additional operator % which is used as an interface between the format specifiers and their values.
In other words, we can say that it binds the format specifiers to the values.\
"""
Integer =10;
Float=1.290;
String ="prasad"
print("i am integer...my value is %d\n i am float..value is %f\n i am stringg my value is %s"%(Integer,Float,String));
